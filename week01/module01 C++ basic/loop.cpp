#include <bits/stdc++.h>
using namespace std;
int main()
{
    int i;
    for (i = 0; i < 4; i++)
    {
        cout << i << endl;
    }
    i = 0;
    while (i < 4)
    {

        cout << i << endl;
        i++;
    }
    i = 0;
    do
    {
        cout << i << endl;
        i++;
    } while (i < 4);
    
    return 0;
}
